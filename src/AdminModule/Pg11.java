    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dt1 = (DefaultTableModel) SupplierTable.getModel();
        if (SupplierTable.getSelectedRowCount() == 1) {
            String SuppIDLabel = SuppIDTextField.getText();
            String SuppNameLabel = SuppNameTextField.getText();
            String phoneLabel = phoneTextField.getText();

            dt1.setValueAt(SuppIDLabel, SupplierTable.getSelectedRow(), 0);
            dt1.setValueAt(SuppNameLabel, SupplierTable.getSelectedRow(), 1);
            dt1.setValueAt(phoneLabel, SupplierTable.getSelectedRow(), 2);

            JOptionPane.showMessageDialog(this, "Update Successfully..");

        } else {
            if (SupplierTable.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table is Empty..");

            }else{
            JOptionPane.showMessageDialog(this, "Please Select Single Row for Update..");
        }
        }
        
    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dt2 = (DefaultTableModel) SupplierTable.getModel();

        if (SupplierTable.getSelectedRowCount() == 1) {
            dt2.removeRow(SupplierTable.getSelectedRow());
        } else {
            if (SupplierTable.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Table is Empty..");
            }else{
            JOptionPane.showMessageDialog(this, "Please Select Single Row for Delete..");
        }

        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void importBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importBtnActionPerformed
        // TODO add your handling code here:
        String filePath = "E:\\Text2.txt";
        File file = new File(filePath);
        
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
            DefaultTableModel model = (DefaultTableModel)SupplierTable.getModel();
            Object[] lines = br.lines().toArray();
            
            for(int i = 0; i < lines.length; i++){
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
            }
            
        } catch (FileNotFoundException ex) {
           java.util.logging.Logger.getLogger(Pg11.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_importBtnActionPerformed

    private void exportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportBtnActionPerformed
        // TODO add your handling code here:
        String filepath="E:\\Text2.txt";
        File file=new File(filepath);
        try {
            FileWriter fw=new FileWriter(file);
            BufferedWriter bw=new BufferedWriter(fw);
            for(int i=0;i<SupplierTable.getRowCount();i++){
                for(int j=0;j<SupplierTable.getColumnCount();j++){
                    bw.write(SupplierTable.getValueAt(i,j).toString()+" ");
                }
                
                bw.newLine();
            }
            bw.close();
            fw.close();
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(Pg11.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_exportBtnActionPerformed

