    private void AddsupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddsupplierActionPerformed
        if(SupplierId.getText().equals("")||SuppName.getText().equals("")||phoneTextField.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please fill up the all data");
        }
        else{
            String[] data={SupplierId.getText(),SuppName.getText(),phoneTextField.getText()};
            DefaultTableModel tab=(DefaultTableModel)SuppTable.getModel();
            tab.addRow(data);
            JOptionPane.showMessageDialog(this,"Added Sucessfully..");
            SupplierId.setText("");
            SuppName.setText("");
            phoneTextField.setText("");
        }
        
        String filepath="D:\\Files Project\\ExportSuppliers.txt";
        File file=new File(filepath);
        try {
            FileWriter fw=new FileWriter(file);
            PrintWriter bw=new PrintWriter(fw);
            for(int i=0;i<SuppTable.getRowCount();i++){
                for(int j=0;j<SuppTable.getColumnCount();j++){
                    bw.write(SuppTable.getValueAt(i,j).toString()+" ");
                }
                
                bw.println();
            }
            bw.close();
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AddsupplierActionPerformed

    private void SuppTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SuppTableMouseClicked
        DefaultTableModel tab=(DefaultTableModel)SuppTable.getModel();
        String productid=tab.getValueAt(SuppTable.getSelectedRow(),0).toString();
        String name=tab.getValueAt(SuppTable.getSelectedRow(),1).toString();
        String phone=tab.getValueAt(SuppTable.getSelectedRow(),2).toString();
        SupplierId.setText(productid);
        SuppName.setText(name);
        phoneTextField.setText(phone);
    }//GEN-LAST:event_SuppTableMouseClicked

    private void DelsuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DelsuppActionPerformed
        DefaultTableModel table=(DefaultTableModel)SuppTable.getModel();
        if(SuppTable.getSelectedRowCount()==1){
            table.removeRow(SuppTable.getSelectedRow());
        }
        else{
            if(SuppTable.getRowCount()==0){
                JOptionPane.showMessageDialog(this,"Table Is Already Empty!");
            }
            else{
                JOptionPane.showMessageDialog(this,"Please selcet any row to remove");
            }
        }
    }//GEN-LAST:event_DelsuppActionPerformed

