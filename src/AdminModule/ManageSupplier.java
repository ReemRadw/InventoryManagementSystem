    private void EditSuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditSuppActionPerformed
        DefaultTableModel tab=(DefaultTableModel)SuppTable.getModel();
        if(SuppTable.getSelectedRowCount()==1){
            String id=SupplierId.getText();
            String name=SuppName.getText();
            String phone=phoneTextField.getText();
            tab.setValueAt(id, SuppTable.getSelectedRow(),0);
            tab.setValueAt(name, SuppTable.getSelectedRow(),1);
            tab.setValueAt(phone, SuppTable.getSelectedRow(),2);
            JOptionPane.showMessageDialog(this,"Update Successfully..");
        }else{
            if(SuppTable.getRowCount()==0){
                JOptionPane.showMessageDialog(this,"Table is Empty..");
            }
            else{
                JOptionPane.showMessageDialog(this,"Please Select Single Row for Update..");
            }
        }
    }//GEN-LAST:event_EditSuppActionPerformed

