    private void AddOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddOrderActionPerformed
            uprice=Integer.valueOf(PriceProd.getText());
            tot=uprice*Integer.valueOf(Quant.getText());
            Vector v=new Vector();
            v.add(OrderId.getText());
            v.add(ProName.getText());
            v.add(Quant.getText());
            v.add(PriceProd.getText());
            v.add(tot);
            DefaultTableModel dt=(DefaultTableModel)OrderTable.getModel();
            dt.addRow(v); 
            JOptionPane.showMessageDialog(this,"Added Sucessfully!");
            total=total+tot;
            TotalOrd.setText("$"+total);
            i++;
            
            
            //this is for placed orders by user
            String filepathorder="D:\\Files Project\\PlacedOrders.txt";
            File fileorder=new File(filepathorder);
            
        try {
            FileWriter fw2=new FileWriter(fileorder);
            BufferedWriter bw2=new BufferedWriter(fw2);
            for(int i=0;i<OrderTable.getRowCount();i++){
                for(int j=0;j<OrderTable.getColumnCount();j++){
                    bw2.write(OrderTable.getValueAt(i,j).toString()+" ");
                }
                bw2.newLine();
            }
            bw2.close();
            fw2.close();
        } catch (IOException ex) {
            Logger.getLogger(OrderDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
            //for store into file
            String filepath="D:\\Files Project\\Profit.txt";
            File file=new File(filepath);
           try {
            FileWriter fw=new FileWriter(file);
            BufferedWriter bw=new BufferedWriter(fw);
            fw.write(String.valueOf(total));
            bw.close();
            fw.close();
           } catch (IOException ex) {
            Logger.getLogger(OrderDashboard.class.getName()).log(Level.SEVERE, null, ex);
          }
           
            
    }//GEN-LAST:event_AddOrderActionPerformed

